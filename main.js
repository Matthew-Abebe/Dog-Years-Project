// This is my age. //
const myAge = 25;
// This is early years. //
let earlyYears = 2;
earlyYears *= 10.5;
// This is later years. //
let laterYears = myAge - 2;
// This is later years multiplied by 4. //
laterYears *= 4;
console.log(earlyYears, laterYears);
// This is my age in dog years. //
myAgeInDogYears = earlyYears + laterYears;
// This is my name with all lowercase letters. //
myName = 'Matthew'.toLowerCase();
// This states my name, human age, and dog age. //
console.log(`My name is ${myName}. I am ${myAge} years old in human years which is ${myAgeInDogYears} years old in dog years.`)
